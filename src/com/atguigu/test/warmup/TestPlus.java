package com.atguigu.test.warmup;

public class TestPlus
{

	public static void main(String[] args)
	{
		int i = 1;
		
		System.out.println("i: "  +   i);
		System.out.println("++i: "+   ++i);
		System.out.println("i++: "+   i++);
		System.out.println("i: "  +     i);
		System.out.println("--i: "+   --i);
		System.out.println("i--: "+   i--);
		System.out.println("i: "  +   i);		
	}

}
